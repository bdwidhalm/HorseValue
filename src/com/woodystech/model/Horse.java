package com.woodystech.model;

public class Horse
{
  private long id;
  private String name;
  private String breed;
  
  
  
  public long getId()
  {
    return id;
  }
  public void setId(long id)
  {
    this.id = id;
  }
  public String getName()
  {
    return name;
  }
  public void setName(String name)
  {
    this.name = name;
  }
  public String getBreed()
  {
    return breed;
  }
  public void setBreed(String breed)
  {
    this.breed = breed;
  }
  
}
